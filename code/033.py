# using if, else, and elif

x = int(input("Enter x : "))

if x < 5:
    print("Less than 5")
elif x < 10:
    print("Less than 10")
else:
    print("10 or more")

# Input:
# 10
# Output:
# 10 or more

# Input:
# 20
# Output:
# 10 or more

# Input:
# 7
# Output:
# Less than 10

# Input:
# 2
# Output:
# Less than 5

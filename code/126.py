# getting keys and values
# of a dictionary

data = {"France" : "Paries",
        "Italy" : "Rome",
        "Germany" : "Berlin"}

print(data.keys())
print(data.values())

# values will correspond to the keys
# in the output : they appear in
# the same order

# Output:
# dict_keys(['France', 'Italy', 'Germany'])
# dict_values(['Paries', 'Rome', 'Berlin'])

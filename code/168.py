# string literal concatenation

# you have a long string and want
# to put it into a small width of
# your editor's window
s = "Hello, " "World!"
# you can split it in two parts
print(s)

# or even into a few lines
s = "Hello, " \
    "World" \
    "!"
print(s)

# Output:
# Hello, World!
# Hello, World!
